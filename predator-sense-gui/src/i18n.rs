/// Translate with format argument (returns String)
pub fn tf(key: &str, args: &[&str]) -> String {
    let template = t(key);
    let mut result = template.to_string();
    for (i, arg) in args.iter().enumerate() {
        result = result.replace(&format!("{{{}}}", i), arg);
    }
    result
}
